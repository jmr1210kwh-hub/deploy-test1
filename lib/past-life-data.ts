export const getPastLife = (name: string) => {
  // Simple deterministic hash
  let hash = 0;
  for (let i = 0; i < name.length; i++) {
    hash = ((hash << 5) - hash) + name.charCodeAt(i);
    hash |= 0; 
  }
  
  const absHash = Math.abs(hash);
  const lifeIndex = absHash % PAST_LIVES.length;
  
  // Random birth year between -30,000 and 2,000
  // use the hash to make it deterministic
  const yearRange = 32000;
  const birthYear = (absHash % (yearRange + 1)) - 30000;
  
  const formattedYear = birthYear < 0 
    ? `BC ${Math.abs(birthYear)}` 
    : `AD ${birthYear}`;
    
  return {
    title: PAST_LIVES[lifeIndex],
    year: formattedYear,
    rawYear: birthYear
  };
};
